package com.company;

public class TransportableCar {
    int weight;
    int length;
    int height;
    int width;
    String regNumber;
    String vinNumber;

    public TransportableCar(int weight, int length, int height, int width, String regNumber, String vinNumber) {
        this.weight = weight;
        this.length = length;
        this.height = height;
        this.width = width;
        this.regNumber = regNumber;
        this.vinNumber = vinNumber;
    }
}
